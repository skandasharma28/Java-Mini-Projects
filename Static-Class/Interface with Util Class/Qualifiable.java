/**
 * An interface with one method that returns
 * a boolean value.
 * 
 * @author  Qi Yang
 * @version 2022-11-22
 */
public interface Qualifiable
{
    /*
     * Determines if an instance qualifies the condition 
     * specified by the claaa implementing the interface.
     * 
     * @return true if the instance qualifies
     *         false otherwise
     */
    boolean qualifies();
}
